package com.ssm.pojo;

public class about {
    private int id;
    private String about_name;
    private String about_time;
    private String about_msg;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getAbout_name() {
        return about_name;
    }

    public void setAbout_name(String about_name) {
        this.about_name = about_name;
    }

    public String getAbout_time() {
        return about_time;
    }

    public void setAbout_time(String about_time) {
        this.about_time = about_time;
    }

    public String getAbout_msg() {
        return about_msg;
    }

    public void setAbout_msg(String about_msg) {
        this.about_msg = about_msg;
    }
}
